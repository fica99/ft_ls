#include "ft_ls.h"

void	output(char **d_names)
{
	short int	i;

	i = -1;
	while (d_names[++i])
	{
		if (d_names[i][0] != '.')
		{
			ft_putstr(d_names[i]);
			ft_putchar(' ');
		}
	}
	ft_putchar('\n');
}
